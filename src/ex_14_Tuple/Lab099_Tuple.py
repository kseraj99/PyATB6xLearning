"""
A tuple is a collection of ordered and immutable (unchangeable) items.

- Ordered → elements have a fixed position
- Immutable → cannot be changed after creation
- Allows duplicates

"""

my_tuple = (1, 2, 3)

print(my_tuple)

print(type(my_tuple))

print(len(my_tuple))

# Tuple with one element
# For making the tuple we have to use at least one comma(,)

new_tuple = (3,)

print(type(new_tuple))

